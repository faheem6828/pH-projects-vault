/***
Subtask-1:

Display sum of all the odd numbers from 81 to 131.

 */
let oddNum = 81;
let oddSum = 0;
while (oddNum <= 131) {
  oddSum = oddSum + oddNum;
  oddNum += 2;
}
console.log('The sum of all the odd number from 81 to 131 is:',oddSum);


